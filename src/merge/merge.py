import sys
import argparse


if __name__ == '__main__':
    parser = argparse.ArgumentParser()
    parser.add_argument('-clstrpath', '--clstrpath', type=str, dest='clstrpath', help='Local Clusters Path', default='app-merge-data')
    parser.add_argument('-mergepath', '--mergepath', type=str, dest='mergepath', help='Merged Clusters Path', default='merged-clstr-data')
    args = parser.parse_args(sys.argv[1:])
    print(f"Local clusters path is {args.clstrpath}")
    print(f" Merged clusters path is {args.mergedpath}")

